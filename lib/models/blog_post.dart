class BlogPost {
  final int id;
  final String title;
  final String body;
  final String image;
  final String slug;
  final String author;
  final String authorId;
  final String timestamp;

  BlogPost({
    this.id,
    this.title,
    this.body,
    this.image,
    this.slug,
    this.author,
    this.authorId,
    this.timestamp,
  });
}
